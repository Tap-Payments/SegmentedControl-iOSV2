/// Content alignment of the segmented control.
///
/// - topLeft: Top left alignment.
/// - top: Top alignment.
/// - topRight: Top right alignment.
/// - left: Left alignment.
/// - center: Center alignment.
/// - right: Right alignment.
/// - bottomLeft: Bottom left alignment.
/// - bottom: Bottom alignment.
/// - bottomRight: Bottom right alignment.
public enum SegmentedItemContentAlignment {
    
    case topLeft
    case top
    case topRight
    case left
    case center
    case right
    case bottomLeft
    case bottom
    case bottomRight
}
